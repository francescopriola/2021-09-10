	public List<Arco> getArchi(Map<String, Business> idMap, String city){
		String sql = "SELECT b1.`business_id` as b1, b2.`business_id` as b2, b1.`latitude` as lat1, b1.`longitude` as long1, b2.`latitude` as lat2, b2.`longitude` as long2 "
				+ "FROM business b1, business b2 "
				+ "WHERE b1.`city` = ? AND b1.`city` = b2.`city` AND b1.`business_id` > b2.`business_id`"; 
		List<Arco> result = new LinkedList<>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1, city);
			ResultSet res = st.executeQuery();
			while (res.next()) {
				Business b1 = idMap.get(res.getString("b1"));
				Business b2 = idMap.get(res.getString("b2"));
				LatLng a = new LatLng(res.getDouble("lat1"), res.getDouble("long1"));
				LatLng b = new LatLng(res.getDouble("lat2"), res.getDouble("long2"));
				Arco arco = new Arco(b1, b2, LatLngTool.distance(a, b, LengthUnit.KILOMETER));
				
				result.add(arco);
			}
			res.close();
			st.close();
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	} 	
